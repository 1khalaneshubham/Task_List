package DayTask;
import java.util.*;

public class DayOneTask2 {
	public static void main(String[] args){
		// Swaping Element
		int a = 10;
		int b = 20;
		
		int temp = 0;
		
		temp = a;
		a = b;
		b = temp;
		
		System.out.println("a:"+ a);
		System.out.println("b:"+ b);
		
		int c = 30;
		int d = 40;
		int e = 50;
		
		int tool = 0;
		
		tool = c;
		c = d;
		d = e;
		e = tool;
		
		System.out.println("c:"+ c);
		System.out.println("d:"+ d);
		System.out.println("e:"+ e);
		
	}
}
